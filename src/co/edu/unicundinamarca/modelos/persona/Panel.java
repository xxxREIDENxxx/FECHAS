    //refrescar la tabla
    public void refreshTableModel()
    {
        ArrayList<Persona> lista_personas = Repositorio.obtenerTodos();
        while (table_model_personas.getRowCount() > 0) {
            table_model_personas.removeRow(0);
        }
        
        for(Persona p : lista_personas)
        {
            String aux = p.getFechaNacimiento();
            String[] data = {Integer.toString(p.getId()), Integer.toString(p.getDocumento()), p.getNombre(), p.getApellido(), aux,Integer.toString(p.getEdad()),p.getTipoPersona()};
            table_model_personas.addRow(data);
        }
    }
    public String convertirFechaString(Date date){
	
        return new SimpleDateFormat("yyyy-MM-dd HH:mm:ss").format(date);
	
    }

