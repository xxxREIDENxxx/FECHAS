    //refescar todos los campos
    private void resetForm() {
        text_documento.setText("");
        text_nombre.setText("");
        text_edad.setText("");
        text_tipoPersona.setText("");
        text_apellido.setText("");
        DC_fechaNacimiento.setDate(null);
    }
    //resetear
    private void resetPersona() {
        persona.setId(0);
        persona.setDocumento(0);
        persona.setEdad(0);
        persona.setNombre("");
        persona.setTipoPersona("");
        persona.setApellido("");
        popularPersona(persona);
    }
    
    private void popularPersona(Persona persona) {
        text_documento.setText(Integer.toString(persona.getDocumento()));
        text_nombre.setText(persona.getNombre());
        text_edad.setText((Integer.toString(persona.getDocumento())));
        text_tipoPersona.setText(persona.getTipoPersona());
        text_apellido.setText(persona.getApellido());
        
        if(persona.getId() != 0){
            button_eliminar.setVisible(true);
            button_guardar.setActionCommand("Editar");
        } else {
            button_eliminar.setVisible(false);
            button_guardar.setActionCommand("Guardar");
        }
    }

