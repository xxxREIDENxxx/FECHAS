    private void button_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_guardarActionPerformed
       
        if( text_documento.getText().isEmpty() ||
            text_nombre.getText().isEmpty() ||
            text_edad.getText().isEmpty() ||
            text_apellido.getText().isEmpty() ||
            text_tipoPersona.getText().isEmpty()
        ){
            JOptionPane.showMessageDialog(this, "Por favor diligencie todo el formulario", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            //casos de los botones
            switch(evt.getActionCommand())
            {
                case "Guardar":
                    String fecha = Repositorio.convertirFechaString(DC_fechaNacimiento.getDate());
                    persona = Persona.crear(0, Integer.parseInt(text_documento.getText()), text_nombre.getText(), text_apellido.getText(), fecha,Integer.parseInt(text_edad.getText()), text_tipoPersona.getText());
                    Repositorio.crear(persona);
                    resetForm();
                    JOptionPane.showMessageDialog(this, "Persona creada satisfactoriamente", "Bien", JOptionPane.INFORMATION_MESSAGE);
                break;
                case "Editar":
                    persona.setDocumento(Integer.parseInt(text_documento.getText()));
                    persona.setNombre(text_nombre.getText());
                    persona.setApellido(text_apellido.getText());
                    persona.setFechaNacimiento(Repositorio.convertirFechaString(DC_fechaNacimiento.getDate()));
                    persona.setApellido(text_apellido.getText());
                    persona.setEdad(Integer.parseInt(text_edad.getText()));
                    persona.setTipoPersona(text_tipoPersona.getText());
                    Repositorio.editar(persona);
                    JOptionPane.showMessageDialog(this, "Persona actualizada satisfactoriamente", "Bien", JOptionPane.INFORMATION_MESSAGE);
                break;
            }
            
            this.refreshTableModel();
            this.resetPersona();
        }
    }//GEN-LAST:event_button_guardarActionPerformed
